"use strict";

const fs = require("fs");

const DEFAULT_LABEL_POLICY = {
  safeLabelPattern: "^ci\\/[a-z0-9][a-z0-9/_-]{0,49}$",
  managedLabelPrefixes: [
    "ci/harness-",
    "ci/severity-",
    "ci/owner-",
    "ci/action-",
    "ci/auto-loop-",
    "ci/trend-",
    "ci/policy-drift-",
    "ci/policy-drift-worsening",
  ],
  defaultColor: "6a737d",
  defaultDescription: "Generated by harness gate automation",
  labelRules: [],
};

const DEFAULT_COMMENT_POLICY = {
  commentMarker: "<!-- harness-gate-summary -->",
  commentTrigger: {
    overallStates: ["fail"],
    trendSeverities: ["warn", "error"],
  },
  commentTemplate: {
    title: "### Harness Gate Signal",
    fields: [
      { key: "overall", label: "overall", format: "code" },
      { key: "trend_tag", label: "trend_tag", format: "code" },
      { key: "trend_severity", label: "trend_severity", format: "code" },
      { key: "policy_drift", label: "policy_drift", format: "code" },
      { key: "auto_loop_state", label: "auto_loop_state", format: "code" },
      { key: "auto_loop_selected_variant", label: "auto_loop_selected_variant", format: "code" },
      { key: "auto_loop_selected_proposal", label: "auto_loop_selected_proposal", format: "code" },
      { key: "auto_loop_circuit_breaker", label: "auto_loop_circuit_breaker", format: "code" },
      { key: "owner", label: "owner", format: "code" },
      { key: "action", label: "action", format: "text" },
      { key: "suggested_labels", label: "suggested_labels", format: "code" },
    ],
  },
};

const POLICY_DRIFT_SEVERITIES = ["high", "medium", "low", "none"];
const POLICY_DRIFT_SEVERITY_ORDER = new Map([
  ["none", 0],
  ["low", 1],
  ["medium", 2],
  ["high", 3],
]);
const POLICY_DRIFT_STATE_MARKER_KEY = "harness-gate-policy-drift-state";
const POLICY_DRIFT_STATE_MARKER_REGEX = /<!--\s*harness-gate-policy-drift-state:(\{[^\n]*\})\s*-->/;

const DEFAULT_POLICY_DRIFT_POLICY = {
  labelPrefix: "ci/policy-drift-",
  worseningAlertThreshold: 2,
  worseningLabel: "ci/policy-drift-worsening",
  commentTriggerSeverities: ["high", "medium"],
  actionHints: {
    high: "Policy schema mismatch detected, sync ci_label_policy immediately before merge.",
    medium: "Policy fields missing, sync policy template before merge.",
    low: "Policy has unknown fields, verify compatibility and clean up drift.",
    none: "n/a",
  },
};

const EXPECTED_SCHEMA = "ci_label_policy";
const EXPECTED_SCHEMA_VERSION = 1;
const EXPECTED_TOP_LEVEL_KEYS = new Set([
  "schema",
  "schema_version",
  "safe_label_pattern",
  "comment_marker",
  "comment_trigger",
  "comment_template",
  "policy_drift",
  "managed_label_prefixes",
  "default_color",
  "default_description",
  "label_rules",
]);

const ALLOWED_OVERALL_STATES = new Set(["pass", "fail", "unknown"]);
const ALLOWED_TREND_SEVERITIES = new Set(["info", "warn", "error"]);
const ALLOWED_POLICY_DRIFT_SEVERITIES = new Set(POLICY_DRIFT_SEVERITIES);

const _readPolicy = (policyPath, core) => {
  try {
    const parsed = JSON.parse(fs.readFileSync(policyPath, "utf8"));
    if (parsed && typeof parsed === "object") {
      return parsed;
    }
    if (core && typeof core.warning === "function") {
      core.warning(`policy file is not object: ${policyPath}`);
    }
  } catch (error) {
    if (core && typeof core.warning === "function") {
      core.warning(`failed to load policy from ${policyPath}: ${error}`);
    }
  }
  return {};
};

const _analyzePolicyShape = (parsed) => {
  const safeParsed = parsed && typeof parsed === "object" ? parsed : {};
  const keys = Object.keys(safeParsed);
  const unknownKeys = keys.filter((key) => !EXPECTED_TOP_LEVEL_KEYS.has(key)).sort();
  const missingKeys = [...EXPECTED_TOP_LEVEL_KEYS].filter((key) => !Object.prototype.hasOwnProperty.call(safeParsed, key));
  const schema =
    typeof safeParsed.schema === "string" && safeParsed.schema.trim()
      ? safeParsed.schema.trim()
      : null;
  const schemaVersion =
    Number.isInteger(safeParsed.schema_version) && typeof safeParsed.schema_version === "number"
      ? safeParsed.schema_version
      : null;
  return {
    schema,
    schemaVersion,
    schemaMatches: schema === EXPECTED_SCHEMA,
    schemaVersionMatches: schemaVersion === EXPECTED_SCHEMA_VERSION,
    unknownKeys,
    missingKeys,
  };
};

const _classifyPolicyDrift = (shape) => {
  if (!shape.schemaMatches || !shape.schemaVersionMatches) {
    return { severity: "high", reason: "schema_mismatch" };
  }
  if (shape.missingKeys.length > 0) {
    return { severity: "medium", reason: "missing_fields" };
  }
  if (shape.unknownKeys.length > 0) {
    return { severity: "low", reason: "unknown_fields" };
  }
  return { severity: "none", reason: "shape_ok" };
};

const _buildPolicyDiagnostics = (parsed) => {
  const shape = _analyzePolicyShape(parsed);
  const drift = _classifyPolicyDrift(shape);
  return {
    severity: drift.severity,
    reason: drift.reason,
    schema: shape.schema,
    schemaVersion: shape.schemaVersion,
    schemaMatches: shape.schemaMatches,
    schemaVersionMatches: shape.schemaVersionMatches,
    unknownKeys: shape.unknownKeys,
    missingKeys: shape.missingKeys,
  };
};

const _emitPolicyDiagnostics = ({ diagnostics, policyPath, core, channel }) => {
  if (!core) {
    return;
  }
  const summary = `severity=${diagnostics.severity}, reason=${diagnostics.reason}, schema=${
    diagnostics.schema || "n/a"
  }, schema_version=${
    diagnostics.schemaVersion === null ? "n/a" : String(diagnostics.schemaVersion)
  }, unknown_keys=${diagnostics.unknownKeys.length}, missing_keys=${diagnostics.missingKeys.length}`;
  const scope = channel ? `[${channel}] ` : "";
  if (diagnostics.severity !== "none") {
    if (typeof core.warning === "function") {
      core.warning(
        `[ci-label-policy] ${scope}shape drift detected for ${policyPath}: ${summary}`
      );
      if (diagnostics.unknownKeys.length > 0) {
        core.warning(
          `[ci-label-policy] ${scope}unknown top-level fields: ${diagnostics.unknownKeys.join(", ")}`
        );
      }
      if (diagnostics.missingKeys.length > 0) {
        core.warning(
          `[ci-label-policy] ${scope}missing top-level fields: ${diagnostics.missingKeys.join(", ")}`
        );
      }
    }
    return;
  }
  if (typeof core.notice === "function") {
    core.notice(`[ci-label-policy] ${scope}shape ok for ${policyPath}: ${summary}`);
  }
};

const _normalizeUniqueEnumList = (values, allowedSet) => {
  if (!Array.isArray(values)) {
    return [];
  }
  const seen = new Set();
  const normalized = [];
  for (const value of values) {
    if (typeof value !== "string") {
      continue;
    }
    const item = value.trim();
    if (!item || !allowedSet.has(item) || seen.has(item)) {
      continue;
    }
    seen.add(item);
    normalized.push(item);
  }
  return normalized;
};

const _normalizePolicyDriftSeverity = (value) => {
  const normalized = typeof value === "string" ? value.trim().toLowerCase() : "";
  return POLICY_DRIFT_SEVERITY_ORDER.has(normalized) ? normalized : "none";
};

const _normalizePolicyDriftReason = (value) => {
  if (typeof value !== "string") {
    return "shape_ok";
  }
  const normalized = value.trim();
  return normalized.length > 0 ? normalized : "shape_ok";
};

const _parseNonNegativeInt = (value) => {
  if (typeof value === "number" && Number.isInteger(value) && value >= 0) {
    return value;
  }
  if (typeof value === "string" && value.trim()) {
    const parsed = Number.parseInt(value.trim(), 10);
    if (Number.isInteger(parsed) && parsed >= 0) {
      return parsed;
    }
  }
  return 0;
};

const _normalizeCommentTemplate = (template) => {
  if (!template || typeof template !== "object") {
    return DEFAULT_COMMENT_POLICY.commentTemplate;
  }
  const title =
    typeof template.title === "string" && template.title.trim()
      ? template.title.trim()
      : DEFAULT_COMMENT_POLICY.commentTemplate.title;
  const fields = Array.isArray(template.fields)
    ? template.fields
        .filter((item) => item && typeof item === "object")
        .map((item) => ({
          key: typeof item.key === "string" && item.key.trim() ? item.key.trim() : "",
          label: typeof item.label === "string" && item.label.trim() ? item.label.trim() : "",
          format: item.format === "text" || item.format === "code" ? item.format : "text",
        }))
        .filter((item) => item.key.length > 0 && item.label.length > 0)
    : [];
  if (fields.length === 0) {
    return { title, fields: DEFAULT_COMMENT_POLICY.commentTemplate.fields };
  }
  return { title, fields };
};

const _normalizeCommentTrigger = (trigger) => {
  if (!trigger || typeof trigger !== "object") {
    return DEFAULT_COMMENT_POLICY.commentTrigger;
  }
  const overallStates = _normalizeUniqueEnumList(trigger.overall_states, ALLOWED_OVERALL_STATES);
  const trendSeverities = _normalizeUniqueEnumList(trigger.trend_severities, ALLOWED_TREND_SEVERITIES);
  if (overallStates.length === 0 && trendSeverities.length === 0) {
    return DEFAULT_COMMENT_POLICY.commentTrigger;
  }
  return { overallStates, trendSeverities };
};

const _normalizePolicyDrift = (policyDrift) => {
  if (!policyDrift || typeof policyDrift !== "object") {
    return DEFAULT_POLICY_DRIFT_POLICY;
  }
  const labelPrefix =
    typeof policyDrift.label_prefix === "string" && policyDrift.label_prefix.trim()
      ? policyDrift.label_prefix.trim()
      : DEFAULT_POLICY_DRIFT_POLICY.labelPrefix;
  const worseningAlertThresholdRaw = policyDrift.worsening_alert_threshold;
  const worseningAlertThreshold =
    Number.isInteger(worseningAlertThresholdRaw) && worseningAlertThresholdRaw > 0
      ? worseningAlertThresholdRaw
      : DEFAULT_POLICY_DRIFT_POLICY.worseningAlertThreshold;
  const worseningLabel =
    typeof policyDrift.worsening_label === "string" && policyDrift.worsening_label.trim()
      ? policyDrift.worsening_label.trim()
      : DEFAULT_POLICY_DRIFT_POLICY.worseningLabel;
  const commentTriggerSeverities = _normalizeUniqueEnumList(
    policyDrift.comment_trigger_severities,
    ALLOWED_POLICY_DRIFT_SEVERITIES
  );
  const actionHintsRaw =
    policyDrift.action_hints && typeof policyDrift.action_hints === "object"
      ? policyDrift.action_hints
      : {};
  const actionHints = Object.fromEntries(
    POLICY_DRIFT_SEVERITIES.map((severity) => {
      const rawValue = actionHintsRaw[severity];
      if (typeof rawValue === "string" && rawValue.trim()) {
        return [severity, rawValue.trim()];
      }
      return [severity, DEFAULT_POLICY_DRIFT_POLICY.actionHints[severity]];
    })
  );
  return {
    labelPrefix,
    worseningAlertThreshold,
    worseningLabel,
    commentTriggerSeverities:
      commentTriggerSeverities.length > 0
        ? commentTriggerSeverities
        : DEFAULT_POLICY_DRIFT_POLICY.commentTriggerSeverities,
    actionHints,
  };
};

const loadCiLabelPolicyForLabels = ({ policyPath, core }) => {
  const parsed = _readPolicy(policyPath, core);
  const diagnostics = _buildPolicyDiagnostics(parsed);
  _emitPolicyDiagnostics({ diagnostics, policyPath, core, channel: "labels" });
  const safeLabelPattern =
    typeof parsed.safe_label_pattern === "string" && parsed.safe_label_pattern.trim()
      ? parsed.safe_label_pattern.trim()
      : DEFAULT_LABEL_POLICY.safeLabelPattern;
  const defaultColor =
    typeof parsed.default_color === "string" && parsed.default_color.trim()
      ? parsed.default_color.trim()
      : DEFAULT_LABEL_POLICY.defaultColor;
  const defaultDescription =
    typeof parsed.default_description === "string" && parsed.default_description.trim()
      ? parsed.default_description.trim()
      : DEFAULT_LABEL_POLICY.defaultDescription;
  const managedLabelPrefixesRaw = Array.isArray(parsed.managed_label_prefixes) ? parsed.managed_label_prefixes : [];
  const managedLabelPrefixes = managedLabelPrefixesRaw
    .filter((item) => typeof item === "string")
    .map((item) => item.trim())
    .filter((item) => item.length > 0);
  const labelRulesRaw = Array.isArray(parsed.label_rules) ? parsed.label_rules : [];
  const labelRules = labelRulesRaw
    .filter((item) => item && typeof item === "object")
    .map((item) => ({
      prefix: typeof item.prefix === "string" && item.prefix.trim() ? item.prefix.trim() : "",
      color: typeof item.color === "string" && item.color.trim() ? item.color.trim() : defaultColor,
      description:
        typeof item.description === "string" && item.description.trim()
          ? item.description.trim()
          : defaultDescription,
    }))
    .filter((item) => item.prefix.length > 0);
  const policyDrift = _normalizePolicyDrift(parsed.policy_drift);

  return {
    safeLabelPattern,
    managedLabelPrefixes:
      managedLabelPrefixes.length > 0
        ? managedLabelPrefixes
        : DEFAULT_LABEL_POLICY.managedLabelPrefixes,
    defaultColor,
    defaultDescription,
    labelRules,
    policyDrift,
    policyDiagnostics: diagnostics,
  };
};

const loadCiLabelPolicyForComment = ({ policyPath, core }) => {
  const parsed = _readPolicy(policyPath, core);
  const diagnostics = _buildPolicyDiagnostics(parsed);
  _emitPolicyDiagnostics({ diagnostics, policyPath, core, channel: "comment" });
  const commentMarker =
    typeof parsed.comment_marker === "string" && parsed.comment_marker.trim()
      ? parsed.comment_marker.trim()
      : DEFAULT_COMMENT_POLICY.commentMarker;
  return {
    commentMarker,
    commentTemplate: _normalizeCommentTemplate(parsed.comment_template),
    commentTrigger: _normalizeCommentTrigger(parsed.comment_trigger),
    policyDrift: _normalizePolicyDrift(parsed.policy_drift),
    policyDiagnostics: diagnostics,
  };
};

const extractPolicyDriftStateFromCommentBody = ({ commentBody, core }) => {
  const fallback = { severity: "none", reason: "shape_ok", worseningStreak: 0 };
  if (typeof commentBody !== "string" || !commentBody.trim()) {
    return fallback;
  }
  const matched = commentBody.match(POLICY_DRIFT_STATE_MARKER_REGEX);
  if (!(matched && matched[1])) {
    return fallback;
  }
  try {
    const parsed = JSON.parse(matched[1]);
    if (!parsed || typeof parsed !== "object") {
      return fallback;
    }
    return {
      severity: _normalizePolicyDriftSeverity(parsed.severity),
      reason: _normalizePolicyDriftReason(parsed.reason),
      worseningStreak: _parseNonNegativeInt(parsed.worsening_streak ?? parsed.worseningStreak),
    };
  } catch (error) {
    if (core && typeof core.warning === "function") {
      core.warning(`failed to parse previous policy drift state from comment: ${error}`);
    }
    return fallback;
  }
};

const buildPolicyDriftReport = ({ policyDiagnostics, policyDrift, previousState }) => {
  const diagnostics = policyDiagnostics && typeof policyDiagnostics === "object" ? policyDiagnostics : {};
  const driftPolicy = policyDrift && typeof policyDrift === "object" ? policyDrift : {};
  const previous = previousState && typeof previousState === "object" ? previousState : {};
  const severity = _normalizePolicyDriftSeverity(diagnostics.severity);
  const reason = _normalizePolicyDriftReason(diagnostics.reason);
  const previousSeverity = _normalizePolicyDriftSeverity(previous.severity);
  const previousReason = _normalizePolicyDriftReason(previous.reason);
  const previousWorseningStreak = _parseNonNegativeInt(
    previous.worseningStreak ?? previous.worsening_streak
  );
  const rawThreshold =
    driftPolicy.worseningAlertThreshold ?? driftPolicy.worsening_alert_threshold;
  const worseningAlertThreshold =
    Number.isInteger(rawThreshold) && rawThreshold > 0
      ? rawThreshold
      : DEFAULT_POLICY_DRIFT_POLICY.worseningAlertThreshold;
  const worseningLabelRaw = driftPolicy.worseningLabel ?? driftPolicy.worsening_label;
  const worseningLabel =
    typeof worseningLabelRaw === "string" && worseningLabelRaw.trim()
      ? worseningLabelRaw.trim()
      : DEFAULT_POLICY_DRIFT_POLICY.worseningLabel;
  const previousRank = POLICY_DRIFT_SEVERITY_ORDER.get(previousSeverity) ?? 0;
  const currentRank = POLICY_DRIFT_SEVERITY_ORDER.get(severity) ?? 0;
  const worseningStreak = currentRank > previousRank ? previousWorseningStreak + 1 : 0;
  const worseningAlert = worseningStreak >= worseningAlertThreshold;
  return {
    severity,
    reason,
    label: `${severity}:${reason}`,
    previous_severity: previousSeverity,
    previous_reason: previousReason,
    worsening_streak: worseningStreak,
    worsening_alert: worseningAlert,
    worsening_alert_threshold: worseningAlertThreshold,
    worsening_label: worseningLabel,
    transition: `${previousSeverity}->${severity}`,
  };
};

const buildPolicyDriftStateMarker = (state) => {
  const normalized = {
    severity: _normalizePolicyDriftSeverity(state?.severity),
    reason: _normalizePolicyDriftReason(state?.reason),
    worsening_streak: _parseNonNegativeInt(state?.worsening_streak ?? state?.worseningStreak),
  };
  return `<!-- ${POLICY_DRIFT_STATE_MARKER_KEY}:${JSON.stringify(normalized)} -->`;
};

